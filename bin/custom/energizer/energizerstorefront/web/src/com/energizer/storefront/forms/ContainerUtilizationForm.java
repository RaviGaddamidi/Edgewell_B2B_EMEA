public class ContainerUtilizationForm
{

	private String containerHeight;
	private String packingType;

	/**
	 * @return the containerHeight
	 */
	public String getContainerHeight()
	{
		return containerHeight;
	}

	/**
	 * @param containerHeight
	 *           the containerHeight to set
	 */
	public void setContainerHeight(final String containerHeight)
	{
		this.containerHeight = containerHeight;

	}

	/**
	 * @return the packingType
	 */
	public String getPackingType()
	{
		return packingType;
	}

	/**
	 * @param packingType
	 *           the packingType to set
	 */
	public void setPackingType(final String packingType)
	{
		this.packingType = packingType;
	}
}
